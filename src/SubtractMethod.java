
public class SubtractMethod {

	int num1 = 10;
	int num2 = 20;

	void subtract() {
		int numSub = num2 - num1;
		System.out.println("The Subtraction of 2 Number is " + numSub);
	}

	void add() {
		int numAdd = num1 + num2;
		System.out.println("The Addition of 2 Number is " + numAdd);

	}

	void Multiply() {
		int numMul = num1 * num2;
		System.out.println("The Addition of 2 Number is " + numMul);

	}

}
